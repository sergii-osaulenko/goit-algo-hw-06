import networkx as nx
import matplotlib.pyplot as plt

# Створення порожнього графа
G = nx.Graph()

# Додавання вершин (Райони міста)
nodes = ["Центр", "Північ", "Південь", "Захід", "Схід", "Вокзал", "Аеропорт", "Парк", "Ринок", "Університет"]
G.add_nodes_from(nodes)

# Додавання ребер (Маршрути між районами)
# Це незважений граф (поки що)
edges = [
    ("Центр", "Вокзал"), ("Центр", "Університет"), ("Центр", "Парк"),
    ("Вокзал", "Північ"), ("Вокзал", "Захід"),
    ("Північ", "Аеропорт"), ("Північ", "Ринок"),
    ("Захід", "Парк"), ("Захід", "Аеропорт"),
    ("Парк", "Південь"),
    ("Південь", "Ринок"), ("Південь", "Схід"),
    ("Схід", "Університет"),
    ("Університет", "Ринок")
]
G.add_edges_from(edges)

# Візуалізація графа
plt.figure(figsize=(10, 8))
pos = nx.spring_layout(G, seed=42)  # seed для фіксованого позиціювання
nx.draw(G, pos, with_labels=True, node_color='lightblue', node_size=2000, font_size=10, font_weight='bold', edge_color='gray')
plt.title("Транспортна мережа міста")
plt.show()

# Аналіз основних характеристик
print(f"Кількість вершин: {G.number_of_nodes()}")
print(f"Кількість ребер: {G.number_of_edges()}")

print("\nСтупінь вершин (кількість з'єднань):")
for node, degree in G.degree():
    print(f"{node}: {degree}")