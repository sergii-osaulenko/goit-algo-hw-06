# Додавання ваги до існуючих ребер (наприклад, відстань у км)
# Для простоти оновимо ребра з вагою
weighted_edges = [
    ("Центр", "Вокзал", 2), ("Центр", "Університет", 5), ("Центр", "Парк", 4),
    ("Вокзал", "Північ", 6), ("Вокзал", "Захід", 3),
    ("Північ", "Аеропорт", 10), ("Північ", "Ринок", 4),
    ("Захід", "Парк", 3), ("Захід", "Аеропорт", 8),
    ("Парк", "Південь", 5),
    ("Південь", "Ринок", 2), ("Південь", "Схід", 3),
    ("Схід", "Університет", 4),
    ("Університет", "Ринок", 3)
]

G.add_weighted_edges_from(weighted_edges)

# Реалізація алгоритму Дейкстри за допомогою NetworkX
# Знаходимо найкоротший шлях між всіма вершинами (приклад для двох конкретних)

shortest_path_dijkstra = nx.dijkstra_path(G, source="Аеропорт", target="Університет", weight='weight')
shortest_path_length = nx.dijkstra_path_length(G, source="Аеропорт", target="Університет", weight='weight')

print(f"\nНайкоротший шлях (Дейкстра) від Аеропорт до Університет: {shortest_path_dijkstra}")
print(f"Довжина шляху (вага): {shortest_path_length}")

